export const allDestinations = [...indianDestinations, ...internationalDestinations];

// Filter helpers
export const getDestinationsByRegion = (region) => {
  return allDestinations.filter(dest => dest.region === region);
};

export const getDestinationById = (id) => {
  return allDestinations.find(dest => dest.id === id);
};

export const getIndianDestinationsByRegion = (region) => {
  return indianDestinations.filter(dest => dest.region === region);
};

export const searchDestinations = (query) => {
  const lowerQuery = query.toLowerCase();
  return allDestinations.filter(dest => 
    dest.name.toLowerCase().includes(lowerQuery) ||
    dest.country.toLowerCase().includes(lowerQuery) ||
    dest.region.toLowerCase().includes(lowerQuery) ||
    dest.category.toLowerCase().includes(lowerQuery)
  );
};



